<center><b>TEVÉKENYSÉG ÉS CÉGTÖRTÉNET</b></center><br>

<p>Salgótarjánban a hulladék elszállítását 1920-tól a helyi vállalkozók lóval fogatolt járművel végezték. Az 1922. évi városi rang elnyerésével, az intézményrendszer kiépülésével, az infrastruktúra fejlődésével, a népesség növekedésével egyre nagyobb lakossági igény mutatkozott a kommunális feladatok - a hulladékszállítástól a takarításig – szervezett keretek közötti ellátására. Ez meg is történt, ám az egyre bővülő feladatok ellátásában mérföldkő 1950 volt, amikor megalakult a Salgótarjáni Köztisztasági Vállalat.
    Az egészséges környezet és az esztétikus városkép kialakításához nélkülözhetetlenné vált az állandó köztisztasági és parkgondozási munka. Ennek érdekében a vállalat átszervezésével 1968-ban két költségvetési üzem alakult: a Salgótarján Mélyépítő és a Salgótarjáni Városgazdálkodási Üzem. Utóbbi tevékenysége 1970-ben a temetők kezelésével is kibővült.</p><br>

<p>A későbbiekben a két üzem tevékenysége egyre inkább összekapcsolódott, ezért 1980-ban fuzionáltak majd 1983-tól a szemétszállítást kiterjesztették a városkörnyéki településekre is. A géppark folyamatos fejlesztésével a szemétszállítás a város mind nagyobb részére terjedt ki. A költségvetési üzemek megszűnését követően, 1991. január 1-jén jogutódlással megalakult a Salgótarjáni Városgazdálkodási Vállalat. Feladatai 1992 elején - a Salgótarjáni Ingatlankezelő Vállalat megszűnésével - kibővültek a házkezelési tevékenységgel. Még ez év április 1-jén viszont a hulladékszállítás kiszervezésre került a RITAS Kft-be. 1993. január 1-jén a városi önkormányzat tulajdonlásában megalakult a VGÜ Kft. Fejlődésnek indult a városüzemeltetési munka, lendületbe jött a környezetvédelmi tevékenység. 1996. március 1-jén a Salgó-Vagyon Kft-hez került a házkezelés, a RITAS Kft. megszűnésével pedig visszakerült a hulladékkezelés. 2012. január 1.-vel a Szakipar és gyorsjavitó szolgálat átkerült a Salgó Vagyon Kft.-hez. 2012. április 1.-jén A városuzemeltetési feladatkörök nagyrészét átvette a Salgotaján Foglalkoztatási Nonprofit Kft. 2013. évtől a cég nonprofit kft.-ként működik a hulladékról szóló 2012 évi. CLXXXV. tv. előírásainak megfelelően.</p><br><br>


