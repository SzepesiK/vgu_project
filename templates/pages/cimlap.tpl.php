<p>Üdvözöljük a 64. éves <b>VGÜ Nonprofit Kft. honlapján</b>. Munkánk igen sokrétű, így honlapunkon keresztül szeretnénk átfogó képet adni a lakosság és vállalkozások számára tevékenységeinkről.</p><br>


<p>Cégünk fő tevékenysége a <b>hulladékgazdálkodás</b>, mely keretében biztosított a városban keletkező települési szilárd hulladék összegyűjtése, szállítása, ártalmatlanítása. Jelenleg a cég települési szilárd hulladékkezelési <b>közszolgáltatási körzete Salgótarján Megyei Jogú Városra és a környező 51 településre terjed ki</b>. A begyűjtött települési szilárd hulladék ártalmatlanítására HDPE műanyag fólia szigeteléssel ellátott, a környezetvédelmi előírásoknak mindenben megfelelő hulladéklerakó telepet üzemeltetünk. A Salgotarján Térségi Hulladéklerakó Telepen épült meg a nyugat-európai színvonalú állati hulladékkezelő és átrakó állomás. A telepen megépített kisállat égető berendezésünkkel házi kedvencek kegyeleti hamvasztását is vállaljuk.</p><br>

<p>Városüzemeltetési tevékenységünk keretében lájuk el a Salgótarján közigazgatási területén a köztisztasági feladatokat beleérve a téli síkosságmentesítést is, valamint üzemeltetjük a fizető parkolás rendszerét.</p><br><br>

<b>Köszönünk minden jószándékú, előremutató észrevételt, mert hisszük, hogy csak együtt lehetünk sikeresek.</b><br>
